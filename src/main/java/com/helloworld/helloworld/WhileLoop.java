package com.helloworld.helloworld;

public class WhileLoop {
    public static void main(String[] args) {
        var counter = 1; // Mendefinisikan variabel 'counter' dengan nilai awal 1

        while (counter <= 10){ // Memulai perulangan while dengan kondisi: selama 'counter' kurang dari atau sama dengan 10
            System.out.println("Perulangan Ke-" + counter); // Mencetak pesan "Perulangan Ke-" dan nilai 'counter'
            counter++; // Mencetak pesan "Perulangan Ke-" dan nilai 'counter'
        }
    }
    
}
