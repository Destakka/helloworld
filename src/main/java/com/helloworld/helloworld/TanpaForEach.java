package com.helloworld.helloworld;

public class TanpaForEach {
    public static void main(String[] args) {
        String[] array = {
            "Desta", "Atika", "Nanda",
            "TI", "Teknik", "UMY"
        };
        for (int i = 0; i < array.length; i++) {
            System.out.println(array[i]);
        }
    }
    
}
