package com.helloworld.helloworld;

public class ForEach {
    public static void main(String[] args) {
        String[] array = { // Mendefinisikan array 'array' yang berisi beberapa string
            "Desta", "Atika", "Nanda",
            "TI", "Teknik", "UMY"
        };
        for (var value : array){ // Mendefinisikan array 'array' yang berisi beberapa string
            System.out.println(value); // Mendefinisikan array 'array' yang berisi beberapa string
        }
    }
    
}
