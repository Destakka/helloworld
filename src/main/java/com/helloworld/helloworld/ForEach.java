package com.helloworld.helloworld;

public class ForEach {
    public static void main(String[] args) {
        String[] array = {
            "Desta", "Atika", "Nanda",
            "TI", "Teknik", "UMY"
        };
        for (var value : array){
            System.out.println(value);
        }
    }
    
}
