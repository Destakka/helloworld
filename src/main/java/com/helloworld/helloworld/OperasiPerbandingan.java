package com.helloworld.helloworld;

public class OperasiPerbandingan {
    public static void main(String[] args) {

        // Mendefinisikan dua variabel integer dengan nilai yang sama
        int value1 = 100;
        int value2 = 100;
    
        System.out.println(value1 > value2); // Membandingkan apakah value1 lebih besar dari value2, kemudian mencetak hasilnya
        System.out.println(value1 < value2); // Membandingkan apakah value1 lebih kecil dari value2, kemudian mencetak hasilnya
        System.out.println(value1 >= value2);// Membandingkan apakah value1 lebih besar atau sama dengan value2, kemudian mencetak hasilnya
        System.out.println(value1 <= value2); // Membandingkan apakah value1 lebih kecil atau sama dengan value2, kemudian mencetak hasilnya
        System.out.println(value1 == value2); // Membandingkan apakah value1 lebih kecil atau sama dengan value2, kemudian mencetak hasilnya
        System.out.println(value1 != value2); // Membandingkan apakah value1 tidak sama dengan value2, kemudian mencetak hasilnya
    }
}
